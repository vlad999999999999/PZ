# короче есть группа и кто к кому ходил в гости

grp = {
    "Анна": {"Борис", "Виктор", "Галина"},
    "Борис": {"Анна", "Виктор"},
    "Виктор": {"Анна", "Борис", "Галина"},
    "Галина": {"Анна", "Борис", "Виктор"}
}

try:
    names = set(grp.keys())  # типо список всех
    visits = {}  # кто сколько раз в гости гонял

    for host, guests in grp.items():  # перебор кто к кому
        for g in guests:
            visits[g] = visits.get(g, 0) + 1  # +1 если был

    need = len(grp) - 1  # ну типо сколько надо раз сходить чтоб у всех побывать
    ok = False  # флажочек челик найден или нет

    for name, cnt in visits.items():  # ищем челика который везде был
        if cnt == need:
            print(f"{name} был у всех.")  # во нашёлся
            ok = True
            break

    if not ok:
        print("никто не был у всех")  # нифига

except Exception as err:
    print("Ошибка:", err)  # если всё сломалось кароч

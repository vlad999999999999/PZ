#Описать функцию PowerA234(A, B, C, D), вычисляющую вторую, третью и
#четвертую степень числа A и возвращающую эти степени соответственно в
#переменные B, C и D (A — входной, B, C, D — выходные параметры; все параметры
#являются вещественными). С помощью этой функции найти вторую, третью и
#четвертую степень пяти данных чисел.


# Функция для вычисления степеней
def PowerA234(A):
    B = A ** 2
    C = A ** 3
    D = A ** 4
    return B, C, D  

numbers = []  # Создаем пустой список для хранения чисел

for i in range(5):
    num = float(input(f"Введите число {i+1}: "))
    numbers.append(num)  # Добавляем введенное число в список

# Перебираем все введенные числа
for num in numbers:
    # Вызов функции и сохранение результатов в переменную
    result = PowerA234(num)

    # Извлекаем каждую степень
    B = result[0]
    C = result[1] 
    D = result[2]

    # Выводим результаты
    print(f"Для числа {num}:")
    print(f"  Вторая степень: {B}")
    print(f"  Третья степень: {C}")
    print(f"  Четвертая степень: {D}\n")

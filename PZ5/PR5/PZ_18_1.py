#Составить программу, в которой функция генерирует четырехзначное число и
#определяет, есть ли в числе одинаковые цифры

import random

def check_same_digits():
    # Генерируем случайное число
    number = random.randint(1000, 9999)
    print(f"Сгенерированное число: {number}")

    d1 = number // 1000           # Первая цифра 
    d2 = (number // 100) % 10     # Вторая цифра
    d3 = (number // 10) % 10      # Третья цифра
    d4 = number % 10              # Четвертая цифра

    # Проверяем, есть ли одинаковые цифры
    if d1 == d2 or d1 == d3 or d1 == d4 or d2 == d3 or d2 == d4 or d3 == d4:
        print(f"Число {number} содержит одинаковые цифры!")
    else:
        print(f"Число {number} не содержит одинаковых цифр.")

check_same_digits()


# Задание 1:
# Дана строка. Все строчные буквы (латин + рус) → заглавные, заглавные → строчные

try:
    s = input("введи строку (любые буквы): ")  # вводим 
    flipped = s.swapcase()  # кароч меняем регистр
    print("результат:", flipped)

except Exception as e:
    print("ошибка:", e)


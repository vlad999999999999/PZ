# Задание 2:
# Дана строка из русских слов с пробелами (может быть много пробелов)
# Вывести те же слова в обратном порядке, с одним пробелом

try:
    txt = input("введи строку из слов с пробелами: ")
    words = txt.split()  # split без аргументов удаляет все лишние пробелы
    rev = ' '.join(reversed(words))  # переворачиваем короч и соединяем
    print("обратный порядок:", rev)

except Exception as e:
    print(e)

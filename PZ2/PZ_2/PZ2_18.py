# Вариант 18. Дано трехзначное число. В нем зачеркнули первую слева цифру и
# приписали ее справа. Вывести 

#Кузнецов Влад

try:
    # Вводим трехзначное число
    number = int(input("Введите трехзначное число: "))

    # Проверяем, что число трехзначное
    if 100 <= number <= 999:
        # Преобразуем число в строку, чтобы легко получить отдельные цифры
        number_str = str(number)
        
        # Формируем новое число, убирая первую цифру и ставя ее в конец
        new_number = int(number_str[1] + number_str[2] + number_str[0])
        
        # Вывод результата
        print("Результат:", new_number)
    else:
        print("Ошибка: введено не трехзначное число.")
except ValueError:
    print("Ошибка: введите корректное целое число.")
